function generateKeywords(title) {
  const keywords = new Set();
  for (const [pattern, kws] of KEYWORD_RULES) {
    if (pattern.test(title)) {
      kws.forEach(k => keywords.add(k));
    }
  }
  return [...keywords];
}

async function fetchPage(pageNum) {
  const url = pageNum === 1
    ? 'https://loosedrawing.com/'
    : `https://loosedrawing.com/?p=${pageNum}`;

  const resp = await fetch(url, {
    headers: {
      'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 Chrome/120 Safari/537.36',
      'Accept': 'text/html,application/xhtml+xml',
    }
  });
  if (!resp.ok) throw new Error(`HTTP ${resp.status} for page ${pageNum}`);
  return await resp.text();
}

function extractIllusts(html) {
  const items = [];
  // /illust/数字/ パターンを抽出
  const linkPattern = /href="https?:\/\/loosedrawing\.com\/illust\/(\d+)\/"/g;
  const titlePattern = /href="https?:\/\/loosedrawing\.com\/illust\/(\d+)\/"[^>]*>([^<]+)</g;

  // タイトルも含めて抽出
  const matches = [...html.matchAll(/href="https?:\/\/loosedrawing\.com\/illust\/(\d+)\/"[^>]*title="([^"]+)"/g)];
  for (const m of matches) {
    items.push({ id: parseInt(m[1]), rawTitle: m[2] });
  }

  // alt属性から抽出
  const altMatches = [...html.matchAll(/href="https?:\/\/loosedrawing\.com\/illust\/(\d+)\/[^"]*"[^>]*>\s*<img[^>]+alt="([^"]+)"/g)];
  for (const m of altMatches) {
    const id = parseInt(m[1]);
    if (!items.find(i => i.id === id)) {
      items.push({ id, rawTitle: m[2] });
    }
  }

  return items;
}

function cleanTitle(raw) {
  // 「〜のイラスト」「〜のイラスト素材」を除去
  return raw
    .replace(/のイラスト素材$/g, '')
    .replace(/のイラスト$/g, '')
    .replace(/フリーイラスト$/g, '')
    .trim();
}

async function buildCatalog() {
  console.log('🚀 Loose Drawing 全カタログ構築開始...');

  // 既存カタログ読み込み（手動キーワード保持用）
  let existingKeywords = {};
  try {
    const existing = JSON.parse(fs.readFileSync(path.join(__dirname, 'illustrations.catalog.json'), 'utf8'));
    for (const item of existing.illustrations) {
      existingKeywords[item.id] = item.keywords;
    }
    console.log(`📚 既存カタログ: ${existing.illustrations.length}件の手動キーワードを保持`);
  } catch(e) {
    console.log('ℹ️ 既存カタログなし、新規作成します');
  }

  const allItems = new Map();
  const TOTAL_PAGES = 62;

  for (let page = 1; page <= TOTAL_PAGES; page++) {
    try {
      console.log(`📄 ページ ${page}/${TOTAL_PAGES} 取得中...`);
      const html = await fetchPage(page);

      // HTMLパース: href="/illust/数字/" + 直後のimg alt="タイトルのイラスト"
      const pattern = /href="\/illust\/(\d+)\/[\s\S]*?alt="([^"]+)"/g;
      for (const m of html.matchAll(pattern)) {
        const id = parseInt(m[1]);
        const title = cleanTitle(m[2].trim());
        if (id && !isNaN(id) && id > 0) {
          allItems.set(id, { id, title });
        }
      }

      // レート制限対策: 0.3秒待機
      await new Promise(r => setTimeout(r, 300));
    } catch(e) {
      console.error(`  ❌ ページ ${page} エラー: ${e.message}`);
    }
  }

  console.log(`\n✅ 全ページ取得完了: ${allItems.size} 件`);

  // タイトルなし（デフォルト）の件数確認
  let noTitle = 0;
  for (const [id, item] of allItems) {
    if (item.title.startsWith('イラスト #')) noTitle++;
  }
  console.log(`  タイトル未取得: ${noTitle} 件`);

  // カタログ構築
  const catalog = [];
  const sortedIds = [...allItems.keys()].sort((a, b) => b - a); // 新しい順

  for (const id of sortedIds) {
    const item = allItems.get(id);
    const title = item.title;

    // キーワード: 手動 > 自動生成
    const keywords = existingKeywords[id] || generateKeywords(title);

    catalog.push({ id, title, keywords });
  }

  // 保存
  const outputPath = path.join(__dirname, 'illustrations.catalog.json');
  fs.writeFileSync(outputPath, JSON.stringify({ illustrations: catalog }, null, 2));

  console.log(`\n🎉 カタログ保存完了!`);
  console.log(`   ファイル: ${outputPath}`);
  console.log(`   総件数: ${catalog.length} 件`);
  console.log(`   手動キーワード保持: ${Object.keys(existingKeywords).length} 件`);
  console.log(`   自動キーワード生成: ${catalog.length - Object.keys(existingKeywords).length} 件`);
}

buildCatalog().catch(console.error);
